    /**
     * Parse the text and change emoticons characters to chearsheet characters
     * @param text Input text to be parsed
     * @return Text parsed
     */
    public static String emojiText(String text)
    {
    	String returnTextString = text;

    	//Pattern to match    	
    	Pattern pattern = Pattern.compile("(\\[[a-z0-9]{5}\\])");
        Matcher matcher = pattern.matcher(text);
        while (matcher.find())
        {
        	String found = matcher.group();
        	if(EmojiMap.get(found)==null)continue;
        	returnTextString = returnTextString.replace(found, EmojiMap.get(found));
        }
    	return returnTextString;
    }

    @TargetApi(Build.VERSION_CODES.GINGERBREAD)
    public static String emojiUTF16Text(String text)
    {
        String returnTextString = text;

        //Pattern to match
        Pattern pattern = Pattern.compile("(\\[[a-z0-9]{5}\\])");
        Matcher matcher = pattern.matcher(text);
        while (matcher.find())
        {
            String found = matcher.group();

            String realfound = found.replace("[","");
            realfound = realfound.replace("]","");
            byte[] foundBytes =  HexString2Bytes(realfound);
            String foundStr = new String(foundBytes, Charset.forName("UTF-32"));
            byte[] foundUTF16 = new byte[0];
            try
            {
                foundUTF16 = foundStr.getBytes("UTF-16BE");
            } catch (UnsupportedEncodingException e) {
                e.printStackTrace();
            }
            String foundCodeStr = ":" + bytesToHexString(foundUTF16) + ":";
            returnTextString = returnTextString.replace(found,foundCodeStr);
        }
        //Returning text
        return returnTextString;
    }
    
